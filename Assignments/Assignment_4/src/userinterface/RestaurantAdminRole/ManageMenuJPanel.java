    private void CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateActionPerformed
        // TODO add your handling code here:
        
        List<Restaurant> resList = ecosystem.getRestaurantDirectory().getRestaurantList();
        
        for(int i = 0; i < resList.size(); i++){
            if(resList.get(i).getRestaurantManager().getUsername().equals(account.getUsername())){
                resList.get(i).getMenuMap().put(createName.getText(), Double.parseDouble(createUsername.getText()));
            }
        }
        
        
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.addRow(new Object[]{
            createName.getText(),
            createUsername.getText()
        });

        JOptionPane.showMessageDialog(null, "Item added to Menu!!");

        createName.setText("");
        createUsername.setText("");
    }//GEN-LAST:event_CreateActionPerformed

    private void Create1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Create1ActionPerformed
        // TODO add your handling code here:
        
        List<Restaurant> resList = ecosystem.getRestaurantDirectory().getRestaurantList();
        
        for(int i = 0; i < resList.size(); i++){
            if(resList.get(i).getRestaurantManager().getUsername().equals(account.getUsername())){
                Map<String, Double> map = resList.get(i).getMenuMap();
                
                for(int j = 0; j < map.size(); j++){
                    if (map.containsKey(updateName.getText())){
                        resList.get(i).getMenuMap().put(updateName.getText(), Double.parseDouble(updateUsername.getText()));
                    }
                }
            }
        }

        int index = customerTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();

        if(index >= 0){
            model.setValueAt(updateName.getText(), index, 0);
            model.setValueAt(updateUsername.getText(), index, 1);
            JOptionPane.showMessageDialog(null, "Item Updated!!");
        }else{
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_Create1ActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        String deleteItem = updateName1.getText();
        boolean flag = false;
        int row = 0;
        
        List<Restaurant> resList = ecosystem.getRestaurantDirectory().getRestaurantList();
        
        for(int i = 0; i < resList.size(); i++){
            if(resList.get(i).getRestaurantManager().getUsername().equals(account.getUsername())){
                Map<String, Double> map = resList.get(i).getMenuMap();
                
                for(int j = 0; j < map.size(); j++){
                    if (map.containsKey(updateName1.getText())){
                        resList.get(i).getMenuMap().remove(updateName1.getText());
                        row = i;
                        flag = true;
                    }
                }
                
            }
        }

        if(flag == false){
            JOptionPane.showMessageDialog(null, "Invalid Data");
        }
        
        if(flag == true){
            model.removeRow(row);
            JOptionPane.showMessageDialog(null, "Item Deleted!!");
            updateName1.setText("");
        }

    }//GEN-LAST:event_DeleteActionPerformed

