    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0);
        
        for(int i = 0; i < customer.getCurrentOrder().getOrderItemList().size(); i++){
            model.addRow(new Object[]{
               customer.getCurrentOrder().getOrderItemList().get(i).getItemName(),
               customer.getCurrentOrder().getOrderItemList().get(i).getPrice().toString(),
               String.valueOf(customer.getCurrentOrder().getOrderItemList().get(i).getQuantity())
            });
        }
    }
